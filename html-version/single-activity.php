           <p>Today, data saves lives. With more and better data, we can save more lives. By pooling and sharing sensitive personal data for biomedical research between countries, we can save more lives faster, making better cures, better diagnosis tools, and better decision support for policy makers.</p>
           <p>Tryggve2 is about developing state-of-the-art scalable infrastructure for safe, efficient, ethical, and legal storage, analysis and sharing of sensitive personal data for biomedical research between countries. The project supports open and transparent data access processes by engaging with the key stakeholders from each of the Nordic countries. It will contribute to facilitating automated cross-border sample and data movements complying with the Nordic legal framework, with an ambition to scale to Europe and beyond.</p>
